  test("framedSerializeWithStats emitSupported=0 no-bounds branch round-trip") {
    assume(
      SparkShimLoader.getSparkVersion.startsWith("4."),
      "Schema-driven parseFramedBytes dispatch is Spark-4.x only")

    // 1-col BIGINT, no bounds: slots 0/1 (lower/upper) null, nullCount=5, count=5.
    val noBoundsStats: InternalRow = new GenericInternalRow(
      Array[Any](null, null, 5, 5, 0L))
    val schema = StructType(Seq(StructField("d", LongType)))
    val statsBlob = CachedColumnarBatchKryoSerializer.serializeStats(noBoundsStats, schema)
    val payload = Array[Byte](0xaa.toByte, 0xbb.toByte, 0xcc.toByte)
    val framed = craftFramed(noBoundsStats, payload)

    val (parsedStats, parsedBytes) =
      CachedColumnarBatchKryoSerializer.parseFramedBytes(framed, schema)

    assert(parsedBytes === payload, "bytesBlob round-trip exact")
    assert(parsedStats !== null, "stats round-trip non-null")
    assert(parsedStats.numFields === 5, "1 col * 5 slots = 5")
    assert(parsedStats.isNullAt(0), "lower bound must be null (no-bounds branch)")
    assert(parsedStats.isNullAt(1), "upper bound must be null (no-bounds branch)")
    assert(parsedStats.getInt(2) === 5, "nullCount=rowCount=5")
    assert(parsedStats.getInt(3) === 5, "count=5")
    assert(parsedStats.getLong(4) === 0L, "sizeInBytes placeholder=0")

    // statsBlob layout sanity: per-col header is 1B supported + 4B nullCount +
    // 4B count + 8B sizeInBytes = 17 bytes; statsBlob = 4B numCols + 17 = 21.
    assert(
      statsBlob.length === 21,
      s"no-bounds 1-col statsBlob length must be 21, got ${statsBlob.length}")
    assert(statsBlob(4) === 0.toByte, "supported byte must be 0 (no-bounds branch)")
  }

  // ---------------------------------------------------------------------------
  // deserializeStats numCols-vs-schema.length invariant (asymmetric).
  // Only the > direction is a corrupt-frame signal; the < direction is allowed
  // because callers may pass an EXPANDED 5-field-per-source-col schema (see
  // ColumnarCachedBatchIntFamilyMarshalSuite for the canonical pattern).
  // 1 RED test (numCols > schema.length) + 2 sentinels (numCols < and schema=null).
  // ---------------------------------------------------------------------------

  test("deserializeStats: numCols < schema.length is allowed (loose-schema API)") {
    // Callers (e.g. IntFamilyMarshalSuite) routinely pass an expanded schema
    // where schema.length = numCols * 5. The first numCols entries drive type
    // dispatch; the rest are silently ignored. Sentinel guards against future
    // tightening that would break the existing test fleet.
    val schema = StructType(Seq(
      StructField("a", LongType),
      StructField("b", LongType),
      StructField("c", LongType),
      StructField("d", LongType),
      StructField("e", LongType)))
    val baos = new java.io.ByteArrayOutputStream()
    writeU32LE(baos, 1) // numCols on wire = 1; schema.length = 5; legal.
    baos.write(0.toByte)
    for (_ <- 0 until 4) baos.write(0.toByte) // nullCount
    for (_ <- 0 until 4) baos.write(0.toByte) // count
    for (_ <- 0 until 8) baos.write(0.toByte) // sizeInBytes
    val row = CachedColumnarBatchKryoSerializer.deserializeStats(baos.toByteArray, schema)
    assert(row != null)
    assert(row.numFields == 5, "1 col * 5 slots = 5; schema.length is NOT the row width")
  }

  test("deserializeStats: numCols > schema.length is rejected with wire-mismatch IAE") {
    val schema = StructType(Seq(StructField("a", LongType)))
    val baos = new java.io.ByteArrayOutputStream()
    writeU32LE(baos, 2) // numCols on wire = 2; schema.length = 1; IOB-bound.
    // two fake col headers (1B + 4B + 4B + 8B = 17B each); the new check
    // fires before they are consumed, so contents are immaterial.
    for (_ <- 0 until 17 * 2) baos.write(0.toByte)
    val blob = baos.toByteArray
    val ex = intercept[IllegalArgumentException] {
      CachedColumnarBatchKryoSerializer.deserializeStats(blob, schema)
    }
    assert(ex.getMessage.contains("numCols=2"), s"got: ${ex.getMessage}")
    assert(ex.getMessage.contains("schema.length=1"), s"got: ${ex.getMessage}")
    assert(ex.getMessage.toLowerCase(Locale.ROOT).contains("wire mismatch"))
  }

  test("deserializeStats: schema=null path unaffected by the new invariant") {
    // V1-wire backcompat: schema=null, numCols read from wire only.
    val baos = new java.io.ByteArrayOutputStream()
    writeU32LE(baos, 1)
    baos.write(0.toByte) // supported=0
    for (_ <- 0 until 4) baos.write(0.toByte) // nullCount
    for (_ <- 0 until 4) baos.write(0.toByte) // count
    for (_ <- 0 until 8) baos.write(0.toByte) // sizeInBytes
    val blob = baos.toByteArray
    val row = CachedColumnarBatchKryoSerializer.deserializeStats(blob, null)
    assert(row != null)
    assert(row.numFields == 5)
  }

  // V3 framing tests

  /** Build a minimal V3 framed byte array with one empty column. */
  private def craftV3Framed(
      statsBlob: Array[Byte],
      numRows: Int,
      numCols: Int,
      colBytesLists: List[Array[Byte]]): Array[Byte] = {
    val out = new java.io.ByteArrayOutputStream()
    // V3 magic
    out.write(Array[Byte](0xfe.toByte, 0xca.toByte, 0x53.toByte, 0x03.toByte))
    writeU32LE(out, statsBlob.length)
    out.write(statsBlob)
    writeU32LE(out, numRows)
    writeU32LE(out, numCols)
    colBytesLists.foreach {
      cb =>
        writeU32LE(out, cb.length)
        out.write(cb)
    }
    out.toByteArray
  }

  test("V3: parseFramedBytes routes magic 0x03 to parseV3Frame") {
    val stats: InternalRow = new GenericInternalRow(Array[Any](1L, 10L, 0, 5, 100L))
    val statsBlob = CachedColumnarBatchKryoSerializer.serializeStats(stats, null)
    val colBytes = Array[Byte](0xab.toByte, 0xcd.toByte) // dummy column bytes
    val framed = craftV3Framed(statsBlob, 5, 1, List(colBytes))

    val (parsedStats, returnedBytes) =
      CachedColumnarBatchKryoSerializer.parseFramedBytes(framed, null)
    // stats should be extracted correctly
    assert(parsedStats != null)
    // bytes = full V3 frame (C++ will parse from magic)
    assert(
      java.util.Arrays.equals(returnedBytes, framed),
      "V3: returned bytes must equal full frame")
  }

  test("V3: statsLen=0 means lazy frame without partition stats") {
    val colBytes = Array[Byte](0xab.toByte, 0xcd.toByte)
    val framed = craftV3Framed(Array.emptyByteArray, 5, 1, List(colBytes))

    val (parsedStats, returnedBytes) =
      CachedColumnarBatchKryoSerializer.parseFramedBytes(framed, StructType(Nil))

    assert(parsedStats === null, "V3 no-stats frame should expose null stats to buildFilter")
    assert(
      java.util.Arrays.equals(returnedBytes, framed),
      "V3 no-stats parser should return the full frame for native projected read")
  }

  test("V3: wrong magic version throws with clear message") {
